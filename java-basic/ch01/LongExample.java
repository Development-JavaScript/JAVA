package ch01.sec09;

public class LongExample {
	public static void main(String[] args){
		//long
		long var1 = 10;
		long var2 = 20L;
		long ver3 = 10000000000000000l;
		long ver4 = 1100000L;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(ver3);
		System.out.println(ver4);
	}
}
